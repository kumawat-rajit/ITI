<p style="text-align: left;"><span style="font-family: arial, helvetica, sans-serif; color: #000000;"><strong>Electrician General</strong>; installs, maintains and repairs electrical machinery equipment and fittings in factories, workshops power house, business and residential premises etc. Studies drawings and other specifications to determine electrical circuit, installation details etc. Positions and installs electrical motors, transformers, switch-gears. Switchboards and other electrical equipment, fittings and lighting fixtures. Makes connections and solders terminals. Tests electrical installations and equipment and locates faults using megger, test lamps etc. Repairs or replaces defective wiring, burnt out fuses and defective parts and keeps fittings and fixtures in working order. May do armature winding, draw wires and cables and do simple cable jointing. May operate, attend and maintain electrical motors, pumps etc.</span></p>
<p><span style="font-family: arial, helvetica, sans-serif; color: #000000;"><strong>Electrical Fitter</strong>; fits and assembles electrical machinery and equipment such as motors,</span></p>
<p><span style="font-family: arial, helvetica, sans-serif; color: #000000;">transformers, generators, switch-gears, fans etc., Studies drawings and wiring diagrams of fittings, wiring and assemblies to be made. Collects prefabricated electrical and mechanical components according to drawing and wiring diagrams and checks them with gauges, megger etc. to ensure proper function and accuracy. Fits mechanical components, resistance, insulators, etc., as per specifications, doing supplementary tooling where necessary. Follows wiring diagrams, makes electrical connections and solders points as specified. Checks for continuity, resistance, circuit shorting, leakage, earthing, etc. at each stage of assembly using megger, ammeter, voltmeter and other appliances and ensures stipulated performance of both mechanical and electrical components filled in assembly.</span></p>
<p><span style="font-family: arial, helvetica, sans-serif; color: #000000;">Erects various equipment such as bus bars, panel boards, electrical posts, fuse boxes switch gears, meters, relays etc. using non-conductors, insulation hoisting equipment as necessary for receipt and distribution of electrical current to feeder lines. Installs motors, generators, transformer etc. as per drawings using lifting and hoisting equipment as necessary, does prescribed electrical wiring, and connects to supply line. Locates faults in case of breakdown and replaces blown out fuse, burnt coils, switches, conductors etc. as required. Checks, dismantles, repairs and overhauls electrical units periodically or as required according to scheduled procedure. May test coils. May specialize in repairs of particular equipment manufacturing, installation or power house work and be designated accordingly.</span></p>
